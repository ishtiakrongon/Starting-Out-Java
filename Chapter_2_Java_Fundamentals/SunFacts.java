package Chapter_2_Java_Fundamentals;

// This program uses E notation

public class SunFacts {
    
    public static void main(String[] args)
    {
        double distance, mass;

        distance = 1.4849654E11;
        mass = 1.2566E30;
        System.out.println("The sun is " + distance +
                                            " meters away");
        System.out.println("The sun's mass is " + mass + " kilograms.");
    }

}
