// A literal is avalue that is written in the code of a program.
// Literals are commonly assigned to variables or displayed. 

package Chapter_2_Java_Fundamentals;

public class Literals {
    public static void main(String[] args) {
        
        int apples;
        
        apples = 20;
        System.out.println("Today we sold " + apples +
                            " bushels of apples.");
    }
}
