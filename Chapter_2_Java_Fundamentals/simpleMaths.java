package Chapter_2_Java_Fundamentals;

public class simpleMaths {
    
    public static void main(String[] args) {

        double a;
        double b;
        double c;

        a = 15.5;
        b = 26.5;
        c = -82.99;
        // Finding Max values

        double z = Math.max(a, b);
        double y = Math.sqrt(9);
        double x = Math.abs(c);


        System.out.println(z);
        System.out.println(y);
        System.out.println(x);

    }

}
